package org.grad.project.model;

public class LogForm {

    private int id;
    private float temp;
    private String time;

    public int getId() {
        return id;
    }

    public float getTemp() {
        return temp;
    }

    public String getTime() {
        return time;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setTemp(float temp) {
        this.temp = temp;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
